<h1>Game Details</h1>

<p>
  The Game Details view provides a place to edit basic details, but more importantly to set and iterate on external
  documented goals, your pipeline, team roles, and other critical items that can help you get the most out of your
  budget and development time.
</p>

<WitpImage src="/images/Witp/GameDetails.png" alt="You're the most critical detail of your game" />

<h2>Goals</h2>
<p>
  Your goals for the game can be a huge part of your design, prioritization, and development of the game. Taking a few
  minutes up front to put it into words can help save time and, if you this is a team effort, can make sure everyone is
  aligned from the start (or address the places where folks have conflicting goals before major decisions have been
  made).
</p>
<p>
  This is not a throwaway step but it's personal enough to you and your team that I didn't want to box you in with
  structure.
</p>

<h2>Groundwork: Pipeline, Roles, Ownership</h2>
<p>
  Have you decided on a set of milestones for the project? Where you're going to store assets and source code? Details
  on your development pipeline? If you're working with a team, have you decided how you're going to approach leads
  and/or decision-making? Change processes?
</p>
<p>
  There's a lot of key decisions you can make now, instead of over and over again later. This section will include a
  link to an external doc with the initial decisions, that you can iterate on later as you go.
</p>
