<p>You have an interesting concept, defined goals, and the broad plan for how you will work on the game.</p>
<p>The final step of the "Idea" Stage is to look through the business lens at the game.</p>
<p style="clear: both">This includes:</p>
<ul>
  <li>Unique Proposition: Expanding on what makes the game unique</li>
  <li>Audience: Who will play it? Who will buy it? How will they learn about it?</li>
  <li>Resources: What can you do yourself? Where do you need partners, assets, or work for hire?</li>
  <li>Money: What type of revenue are you considering? What are your major costs?</li>
</ul>
<p>
  This is not a 50 page business plan and you aren't expected to know all the right answers. The goal is to consider the
  whole business view and uncover what you do and don't know. Answer what you can and be ok entering temporary answers,
  if needed. Later, as you prototype and do research or user testing, you'll learn more to fill in the questions you
  don't know (and validate the ones you think you do) and get more out of that time.
</p>
<p>
  <i
    >This will likely take 30 minutes to 2 hours, depending on your past experience, if you're solo vs a team, and what
    aspects of your game idea you've been thinking or researching so far.</i>
</p>
