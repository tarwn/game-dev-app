<p>
  Your game is unique, but you can still take advantage of information about other games to help with planning. Sales
  estimates, seeing if TikTok reached more of the right audience than Reddit, etc. can help (vs inventing fire again).
</p>
<p>The goal isn't "make the same game as everyone else", it's "learn from similar-enough games".</p>
<p>
  There are a number of really useful tools out there for performing the research, this will help guide you through
  choosing which dimensions to search on and how to use the information you learn.
</p>
