<p>
  An overall project plan will help you coordinate, if you have teammates or external folks doing contract Worker, as
  well as help you know if you're making the progres you think you are (so you can adjust your dates or dig back out if
  you've gone down a rabbit hole).
</p>
<p>
  You don't want to build a detailed project plan at this stage, what you're aiming for is a general plan for the
  project, what the major phases will be, down to roughly how many weeks or months you'll expect to need for aspects of
  the game.
</p>
<p>
  Don't go too deep on the detail at this point, this will help inform activities like cost forecasting (can I afford
  it), scoping, where you prototype, etc.
</p>
