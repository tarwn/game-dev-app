<p>
  Creating a Cost Forecast can help identify if what you've planned so far is possible, given your current resources.
</p>
<p>
  The Cash Forecast module in LaunchReady has multiple stages, "Cost Viability" is the first and will help guide through
  the major costs associated with developing the game, taking care of the math and some of the complexity for you.
</p>
<p>
  When you are done with this step, you will have perspective on what your current plan will cost, whether you can
  afford to make it to launch and beyond, and the output of experimenting with funding or different cost trade-offs.
  Later this will be expaanded into a full cash forecast (with sales estimates) and then an ongoing budget.
</p>
<p>
  You can read an overview in <a href="https://www.launchready.co/blog/create-a-cash-forecast/"
    >Creating a Cash Forecast</a> or go directly to the "Cash Forecast" module above.
</p>
