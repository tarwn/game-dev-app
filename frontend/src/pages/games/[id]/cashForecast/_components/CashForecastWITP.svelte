<h1>Working with Cash Forecasts</h1>

<p>Cash forecasting is a super useful tool throughout the development process.</p>

<div class="indented">
  <p>
    LaunchReady starts with a simpler, cost-based forecast, then progressively layers in more detail as you get deeper
    into developing your concept and game.
  </p>

  <ol>
    <li>Cost Viability: Very early, "Can I afford to build this?"</li>
    <li>Profit Viability: Early, "Can this game reach my goals?"</li>
    <li>Harnessing comparables: Ongoing, "Still on track? Any surprises?"</li>
    <li>Exploring: Ongoing, "What if I contract out this audio? What if an expense blows up?"</li>
  </ol>
</div>

<h1>Navigating the screens</h1>

<p>
  Cash Forecast starts in overview mode, showing you the big chart and details of your cash forecast so far (it's empty
  when you first get here, don't worry!).
</p>

<p>The "Edit" button opens the main interface:</p>

<img src="/images/witp/CashForecastGettingStarted.png" alt="Getting started on the Cash Forecast screen" />

<p>
  From here, tabs each reflect a category of expense, estimated revenue, or details and additional ones will be
  displayed for later stages, listed above.
</p>

<img src="/images/witp/CashForecastTabsOverview.png" alt="Tabs on the Cash Forecast screen" />

