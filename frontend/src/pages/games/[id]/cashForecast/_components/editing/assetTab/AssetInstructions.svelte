<p>
  We start with <b>Assets & Funding</b> to identify the cash we have on hand now plus any other major sources of cash before
  launch.
</p>
<p>
  <b>Bank Balance:</b> The total cash on hand as of the <em>Forecast Start Date</em>
</p>
<p>
  <b>Loans & Credit:</b> This can be used to identify everything from standard bank loans or lines of credit or even personal
  loans. These will generally be paid back at once or in monthly installments.
</p>
<p>
  <b>Funding:</b> These tend to be cash received as investments, publishing partnerships, or similar that tend to expect
  a percentage of gross or net revenue. Grants can also be entered, leaving off the Repayment option.
</p>
